package konec.superchinese.models.vippackages;

public class SpcVIPPackagesData {
	public String packageCode;
	public String name;
	public Integer sellPrice;
	public String cycle;
	public Integer status;
	public Integer originalPrice;
	public String appType;
	public String description;
	public String totalPurchased;
	public String licensePrefix;
	public String getPackageCode() {
		return packageCode;
	}
	public void setPackageCode(String packageCode) {
		this.packageCode = packageCode;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getSellPrice() {
		return sellPrice;
	}
	public void setSellPrice(Integer sellPrice) {
		this.sellPrice = sellPrice;
	}
	public String getCycle() {
		return cycle;
	}
	public void setCycle(String cycle) {
		this.cycle = cycle;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public Integer getOriginalPrice() {
		return originalPrice;
	}
	public void setOriginalPrice(Integer originalPrice) {
		this.originalPrice = originalPrice;
	}
	public String getAppType() {
		return appType;
	}
	public void setAppType(String appType) {
		this.appType = appType;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getTotalPurchased() {
		return totalPurchased;
	}
	public void setTotalPurchased(String totalPurchased) {
		this.totalPurchased = totalPurchased;
	}
	public String getLicensePrefix() {
		return licensePrefix;
	}
	public void setLicensePrefix(String licensePrefix) {
		this.licensePrefix = licensePrefix;
	}
}
